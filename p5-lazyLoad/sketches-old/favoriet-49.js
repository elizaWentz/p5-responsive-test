function poly(points) {
  beginShape();
  for (const p of points) vertex(p[0], p[1]);
  endShape(CLOSE);
}

function addPaperTexture() {
  loadPixels();

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const i = 4 * (y * width + x);
      const n = noise(x * 0.018, y * 0.018);
      const grain = random(-11, 11) + map(n, 0, 1, -8, 8);

      pixels[i] = constrain(pixels[i] + grain, 0, 255);
      pixels[i + 1] = constrain(pixels[i + 1] + grain, 0, 255);
      pixels[i + 2] = constrain(pixels[i + 2] + grain, 0, 255);
      pixels[i + 3] = 255;
    }
  }

  updatePixels();

  blendMode(MULTIPLY);
  noStroke();

  for (let i = 0; i < 4200; i++) {
    const a = random(5, 18);
    fill(255, 245, 220, a);
    circle(random(width), random(height), random(0.45, 1.6));
  }

  blendMode(BLEND);
}