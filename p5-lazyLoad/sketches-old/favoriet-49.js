
function setup() {
  createCanvas(1920, 1080);
  noLoop();
}

function draw() {
  pixelDensity(1);
  background("#e96522");

  const tile = 600;
  const scaleToHeight = height / tile;
  const drawnTile = tile * scaleToHeight;

  for (let x = -drawnTile; x < width + drawnTile; x += drawnTile) {
    push();
    translate(x, 0);
    scale(scaleToHeight);
    drawPatternTile();
    pop();
  }

  addPaperTexture();
}

