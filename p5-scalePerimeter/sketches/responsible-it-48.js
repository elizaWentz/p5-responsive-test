function setup() {
  createCanvas(W, H);
  pixelDensity(1);
  noLoop();
  randomSeed(736);
  noiseSeed(736);
  grain = createGraphics(W, H);
  grain.pixelDensity(1);
  makeGrain();
}

function draw() {
  background(PAPER);

  const marginX = 44;
  const marginY = 26;
  const gutter = 18;
  const cellW = (width - marginX * 2 - gutter * 9) / 10;
  const cellH = (height - marginY * 2 - gutter * 9) / 10;

  noStroke();
  for (let r = 0; r < 10; r++) {
    for (let c = 0; c < 10; c++) {
      const x = marginX + c * (cellW + gutter);
      const y = marginY + r * (cellH + gutter);

      fill(BLUE);
      rect(x, y, cellW, cellH);

      const pts = redCells[r][c];
      if (pts && pts.length > 2) {
        fill(RED);
        beginShape();
        for (let i = 0; i < pts.length; i++) {
          vertex(x + pts[i][0] * cellW, y + pts[i][1] * cellH);
        }
        endShape(CLOSE);
      }
    }
  }

  image(grain, 0, 0);
}

function makeGrain() {
  grain.clear();
  grain.loadPixels();

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const i = (y * width + x) * 4;
      const n = noise(x * 0.035, y * 0.035);
      const speck = random();
      const v = 118 + n * 55 + random(-18, 18);
      const a = speck > 0.972 ? random(34, 58) : random(4, 12);

      grain.pixels[i] = v;
      grain.pixels[i + 1] = v;
      grain.pixels[i + 2] = v;
      grain.pixels[i + 3] = a;
    }
  }

  grain.updatePixels();
}